# -*- coding: UTF-8 -*-
# 基本数据类型
# 1、Number
a = 100  # int
b = 3.14  # float
c = True # bool
# d = 1 + 2i # complex
print(a, b, c)

# 2、String
cat = 'String'  # string
print(cat)
# 3、List
myList = [1, True, 'heheh', [1, 2, 3]]
print(myList[3])

# 4、Tuple
myTuple = (222, '元组', [12, 2], ('11', 22))
print(myTuple[3])

# 5、Set
mySet = {222, 'String'}
print(mySet)

# 6、Dictionary
stu = { 'name':'hah', 'age':12 }
print(stu['name'])